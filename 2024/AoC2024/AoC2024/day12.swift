    func neighbours(for pos: (Int, Int)) -> [(Int, Int)] {
        return [
            (pos.0 - 1, pos.1),
            (pos.0 + 1, pos.1),
            (pos.0, pos.1 - 1),
            (pos.0, pos.1 + 1),
        ]
    }
    
    func isInside(N: Int, M: Int, pos: (Int, Int)) -> Bool {
        return pos.0 >= 0 && pos.0 < N && pos.1 >= 0 && pos.1 < M
    }
    
    func flood(input: [String], for char: Character, from: (r: Int, c: Int)) -> [(Int, Int)] {
        let N = input.count
        let M = input[0].count

        var visited = [[Bool]](repeating: [Bool](repeating: false, count: M), count: N)
        var result = [(Int, Int)]()
        
        var queue = [(Int, Int)]()
        queue.append(from)
        
        while !queue.isEmpty {
            let currentPos = queue.removeFirst()
            visited[currentPos.0][currentPos.1] = true

            result.append(currentPos)

            let neigh: [(Int, Int)] = neighbours(for: currentPos).filter {
                isInside(N: N, M: M, pos: $0)
            }
            
            for n in neigh {
                let line = input[n.0]
                let nCh = line[ line.index(line.startIndex, offsetBy: n.1) ]
                if !visited[n.0][n.1] && nCh == char {
                    visited[n.0][n.1] = true
                    queue.append(n)
                }
            }
        }
        
        return result
    }
    
