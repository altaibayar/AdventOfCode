                if visited[i][j] { continue }
                
                let currentBlob = flood(input: input, for: ch, from: (r: i, c: j))
                
                let area = currentBlob.count
                var perimeter = 0
                
                for (r, c) in currentBlob {
                    // print("\(ch): (\(r), \(c))")

                    let neigh = neighbours(for: (r, c))
                    for (nr, nc) in neigh {
                        if !isInside(N: N, M: M, pos: (nr, nc)) {
                            perimeter += 1
                            continue
                        }
                        
                        let nLine = input[nr]
                        if nLine[ nLine.index(nLine.startIndex, offsetBy: nc) ] != ch {
                            perimeter += 1
                        }
                    }
                    
                    
                    visited[r][c] = true
                }
                
                result += (area * perimeter)
            }
        }
        
